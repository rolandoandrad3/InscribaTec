    private void btnGuardarCarreraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCarreraActionPerformed
        // TODO add your handling code here:
        try {
            if(txtCodigoCarrera.getText().isEmpty()||txtNombreCarrera.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "No puedes dejar campos vacios");
            }
            else{
                PreparedStatement ps = cn.prepareStatement("INSERT INTO Carreras (Nombre_Carrera, Codigo_Carrera) VALUES (?, ?)");
                ps.setString(1, txtNombreCarrera.getText());
                ps.setString(2, txtCodigoCarrera.getText());
                
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Carrera agregada con exito");
                mostrarTabla("");
                limpiar();
            }
        } catch (SQLException e) {
            System.err.println(e);
            JOptionPane.showMessageDialog(null, "Problemas al guardar carrera. Contacte al administrador");
        }
    }//GEN-LAST:event_btnGuardarCarreraActionPerformed

