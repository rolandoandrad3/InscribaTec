    private void btnActualizarCarreraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarCarreraActionPerformed
        // TODO add your handling code here:
        int fila = tblCarreras.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "Por favor, selecciona una fila de la tabla para actualizar.");
            return;
        }
        // Obtener el ID_Carrera de la fila seleccionada
        String idCarrera = tblCarreras.getValueAt(fila, 0).toString();
        try {
            PreparedStatement ps = cn.prepareStatement("UPDATE Carreras SET Nombre_Carrera = ?, Codigo_Carrera = ? WHERE ID_Carrera = ?");
             // Asignar los valores a los parámetros
            ps.setString(1, txtNombreCarrera.getText()); // Nombre_Carrera
            ps.setString(2, txtCodigoCarrera.getText()); // Codigo_Carrera
            ps.setInt(3, Integer.parseInt(idCarrera)); // ID_Carrera desde la tabla
            
            int respuesta = ps.executeUpdate();
            if(respuesta>0){
                JOptionPane.showMessageDialog(null, "Carrera Actualizada");
                limpiar();
                mostrarTabla("");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "No se ha selecionado ninguna fila");
            }
        } catch (SQLException e) {
            System.err.println(e);
            JOptionPane.showMessageDialog(null, "Error al actualizar carrera. Contacte al administrador");
        }
    }//GEN-LAST:event_btnActualizarCarreraActionPerformed

    private void popEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_popEliminarActionPerformed
        // TODO add your handling code here:
        int fila = tblCarreras.getSelectedRow();

        // Obtener el ID_Carrera de la fila seleccionada
        String idCarrera = tblCarreras.getValueAt(fila, 0).toString();
        try {
            PreparedStatement ps = cn.prepareStatement("DELETE FROM Carreras WHERE ID_Carrera='"+idCarrera+"'");
            int respuesta= ps.executeUpdate();
            if(respuesta>0){
                JOptionPane.showMessageDialog(null, "Curso eliminado con exito");
                limpiar();
                mostrarTabla("");
            }
            else{
                JOptionPane.showMessageDialog(null, "Debes seleccionar una fila para borrar");
            }
        } catch (SQLException e) {
            System.err.println(e);
            JOptionPane.showMessageDialog(null, "Error al eliminar carrera. Contacte al administrador");
        }
    }//GEN-LAST:event_popEliminarActionPerformed

